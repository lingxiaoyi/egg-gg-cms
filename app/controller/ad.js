  async index() {
    const ctx = this.ctx;
    const createRule = {
      projectId: 'int',
      qidId: 'int',
      pageId: 'int',
    };
    const query = { projectId: toInt(ctx.query.projectId), qidId: toInt(ctx.query.qidId), pageId: toInt(ctx.query.pageId) };
    ctx.validate(createRule, query);
    ctx.body = await ctx.model.Ads.findAll(
      { where: query }
    );
  }

  async show() {
    const ctx = this.ctx;
    ctx.body = await ctx.model.Ads.findByPk(toInt(ctx.params.id));
  }

  async create() {
    const ctx = this.ctx;
    const { projectId, pageId, qidId, ggId, des } = ctx.request.body;
    const query = { projectId: toInt(projectId), qidId: toInt(qidId), pageId: toInt(pageId), ggId, des };
    const createRule = {
      projectId: 'int',
      pageId: 'int',
      qidId: 'int',
      ggId: {
        type: 'string',
        required: false,
      },
      des: {
        type: 'string',
        required: false,
      },
    };
    query.ggId = query.ggId.replace(/\s+/g, '');
    query.des = query.des.replace(/\s+/g, '');
    ctx.validate(createRule, query);
    const ads = await ctx.model.Ads.create(query);
    ctx.status = 201;
    ctx.body = ads;

  }

